//"Hello, Go"を出力
package main

func main() {
    println("Hello, Go")
}

//数値・文字列の出力
package main

func main() {
    // 数値の7を出力
    println(7)
    
    // 9に3足した値を出力
    println(9 + 3)
    
    // 「9 + 3」を文字列として出力
    println("9 + 3")
    
}

//掛け算・割り算の出力
package main

func main() {
    // 12を3で割った値を出力してください
    println(12 / 3)
    
    // 3に6を掛けた値を出力してください
    println(3 * 6)
    
    // 8を3で割った時の余りを出力してください
    println(8 % 3)
    
}

